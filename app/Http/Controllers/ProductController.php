            'product_title' => 'required|max:30',
            'product_price' => 'required|max:30',
            'product_content' => 'required|max:225',
            'product_photo' => 'required|max:100',
        ]);

        try {
            Product::create($dataValidation);
        } catch (\Exception $e) {
           return $e;
        }
    }

